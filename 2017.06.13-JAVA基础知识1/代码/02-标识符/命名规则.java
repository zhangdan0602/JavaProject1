/*
命名规则：
	包：就是硬盘上的一个文件夹，为了区分相同的类名
		包名一般是以公司的域名反转之后的前两位，www.edu360.cn->cn.edu360
												www.baidu.com->com.baidu
		一个单词：全部小写
		两个单词：全部小写，多个单词之间用"."隔开
	接口和类：
		一个单词：首字母大写	Person
		多个单词：每个单词的首字母都大写 InputSteram
	方法和变量：
		一个单词：全部小写	age
		多个单词：从第二个单词开始首字母大写	setAge
	常量：
		一个单词：全部大写	KEY
		多个单词：全部大写，多个单词之间用“_”隔开	USER_KEY
*/